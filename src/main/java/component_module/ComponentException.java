package component_module;

/**
 * @author Hamza Ouni
 */
public class ComponentException extends Exception {

    public ComponentException() {
    }

    public ComponentException(String arg0) {
        super(arg0);
    }

    public ComponentException(Throwable arg0) {
        super(arg0);
    }

    public ComponentException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
